async def seed() -> None:
    await init_db()
    async with async_session() as s:
        # Проверяем, уже заполнено?
        from sqlalchemy import select, func
        result = await s.execute(select(func.count()).select_from(Tour))
        count = result.scalar()
        if count >= 50:
            print(f"✅ В базе уже {count} туров, повторная загрузка не нужна.")
            return

        for t in TOURS:
            tour = Tour(
                title=t["title"],
                country=t["country"],
                price=t["price"],
                dates=t["dates"],
                description=t["description"],
                added_by=0,  # 0 = system seeded
            )
            s.add(tour)
        await s.commit()
